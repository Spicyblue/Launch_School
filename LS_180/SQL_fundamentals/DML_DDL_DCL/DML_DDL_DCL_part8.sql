-- DML/DDL/DCL Part 8
-- Does the following statement use the DDL or DML component of SQL?

DELETE FROM things WHERE item = 'Cup';

-- Solution
-- The code above uses DML component of SQL since we try to delete a row the schema using the DELETE keyword.

-- LS Note.
-- DELETE statements delete specific rows of data in a database.
-- Therefore, it is part of the DML sublanguage since it manipulates the data and not the structure of the data.