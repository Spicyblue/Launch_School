-- DML/DDL/DCL Part 1
-- SQL consists of 3 different sublanguages. 
-- For example, one of these sublanguages is called the Data Control Language (DCL) component of SQL.
-- It is used to control access to a database; 
-- it is responsible for defining the rights and roles granted to individual users. C
-- Common SQL DCL statements include:

-- Copy Code
-- GRANT
-- REVOKE

-- Name and define the remaining 2 sublanguages, and give at least 2 examples of each.

-- Solution
-- The second sublanguage is Data Manipulation Language (DML).
-- DML is used to retrieve or modify data stored in a relational database.
-- Common SQL DML statements includes:
-- SELECT
-- INSERT
-- UPDATE

-- The third sublanguage us Data Definition Language (DDL).
-- DDL allows users to create and modify the schema stored within the database.
-- Some common DDL statement includes:
-- DROP
-- CREATE 
-- ALTER