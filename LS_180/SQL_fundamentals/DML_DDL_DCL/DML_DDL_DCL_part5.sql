-- DML/DDL/DCL Part 5
-- Does the following statement use the DDL or DML component of SQL?

INSERT INTO things VALUES (3, 'Scissors', 'Metal');

-- Solution
-- The following code uses DML componet of SQL by making using of the INSERT statement.

-- LS Note
-- INSERT adds new rows of data into a database. 
-- Therefore, it is part of the DML sublanguage since it manipulates the data and not the structure of the data.